#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;
	char	aux;
	char	*str;

	aux = (char)c;
	c = aux;
	str = (char *)s;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == c)
			return (&str[i]);
		i++;
	}
	if (c == '\0')
		return (&str[i]);
	return (NULL);
}
